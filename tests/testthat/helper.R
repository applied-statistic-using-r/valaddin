make_fnc <- function(args, body = quote(NULL), env = parent.frame()) {
  args <- as.pairlist(args)
  f <- eval(call("function", args, body))
  environment(f) <- env
  f
}

pass_args <- function(args) {
  body <- substitute({
    call <- match.call()
    call[[1L]] <- as.name("list")
    eval(call)
  })
  make_fnc(args, body)
}